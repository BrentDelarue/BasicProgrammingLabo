getal1 = int(input("Geef een getal op: "))
getal2 = int(input("Geef een tweede getal op: "))

if (getal1 == getal2):
    print("De twee opgegeven getallen {} en {} zijn gelijk aan elkaar.".format(getal1, getal2))
else:
    print("De twee opgegeven getallen {} en {} zijn niet gelijk aan elkaar.".format(getal1, getal2))
